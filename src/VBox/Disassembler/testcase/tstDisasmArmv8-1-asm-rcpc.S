#include <iprt/asmdefs-arm.h>

BEGINPROC_HIDDEN TestProcA64Rcpc

        .cpu generic+rcpc

        ldaprb w0, [x1]
        ldaprb wzr, [sp]

        ldaprh w0, [x1]
        ldaprh wzr, [sp]

        ldapr w0, [x1]
        ldapr wzr, [sp]
        ldapr x0, [x1]
        ldapr xzr, [sp]

        /*
         * Keep last so the testcase can catch errors in
         * the disassembly of the last instruction.
         */
        nop

BEGINPROC_HIDDEN TestProcA64Rcpc_EndProc
